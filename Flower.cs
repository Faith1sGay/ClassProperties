public class Flower
{
    string _name;
    string _color;
    int _petals;
    public string Color
    {
        get { return _color; }
        set { _color = value; }
    }
    public int Petals
    {
        get { return _petals; }
        set { _petals = value; }
    }
    public string Name
    {
        get { return _name; }
        set { _name = value; }
    }
}